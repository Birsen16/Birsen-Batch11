package Loops.forLoop;

public class NestedPractice {
    public static void main(String[] args) {
        for (int i = 0; i<3;i++){
            System.out.println("I am the first Loop");
            for (int j = 0; j <5 ; j++){
                System.out.println("I am Nested for Loop");
            }
        }
        System.out.println("end of the execution");










    }
}
